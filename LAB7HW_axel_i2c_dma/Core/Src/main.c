void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim){
	if(htim->Instance == htim1.Instance){
		  uint8_t CTRL_DATA;

		  CTRL_DATA = LIS2DE_OUT_X_H | 0x80; //it's required for sequential write to set the MSB of this reg to 1
		  if(HAL_I2C_Master_Transmit(&hi2c1, LIS2DE_axel_addr_write, &CTRL_DATA, 1, 100) != HAL_OK){
			  Error_Handler();
		  }

		 if(HAL_I2C_Master_Receive_DMA(&hi2c1, LIS2DE_axel_addr_read, (uint8_t *) i2c_readbuffer, 6) != HAL_OK){
			  Error_Handler();
		  }
	}

}

void HAL_I2C_MasterRxCpltCallback(I2C_HandleTypeDef *hi2c)
{
	char message[100];
	int message_length = snprintf(message,100,"X: %+.2f\r\nY: %+.2f\r\nZ: %+.2f\r\n\r\n",
			i2c_readbuffer[0]/64.0, i2c_readbuffer[2]/64.0, i2c_readbuffer[4]/64.0);
	HAL_UART_Transmit_DMA(&huart2, (uint8_t *)message, message_length);
}

